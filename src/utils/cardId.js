function getRandCardId(ymd, address) {
  var coefficientArray = ["7", "9", "10", "5", "8", "4", "2", "1", "6", "3", "7", "9", "10", "5", "8", "4", "2"];// 加权因子
  var lastNumberArray = ["1", "0", "X", "9", "8", "7", "6", "5", "4", "3", "2"];// 校验码

  // var address = "420101"; // 住址
  // var birthday = "20160103"; // 生日
  var areas = Object.keys(getAreaInfo());
  var index = Math.floor(Math.random() * areas.length);
  var address = address || areas[index];
  var birthday = ymd || getRandYear();
  function getRandYear() {
    var maxY = (new Date()).getFullYear();
    var minY = maxY - 150;
    var y = Math.floor(Math.random() * (maxY - minY + 1) + minY);
    var m = Math.floor(Math.random() * (12 - 1 + 1) + 1);
    var d = 0;
    if ([1, 3, 5, 7, 8, 10, 12].indexOf(m) > -1) {
      d = Math.floor(Math.random() * (31 - 1 + 1) + 1);
    }
    if ([4, 6, 9, 11].indexOf(m) > -1) {
      d = Math.floor(Math.random() * (30 - 1 + 1) + 1);
    }
    if (m == 2) {
      if ((y % 4 == 0 && y % 100 != 0) || y % 400 == 0) {
        d = Math.floor(Math.random() * (28 - 1 + 1) + 1);
      } else {
        d = Math.floor(Math.random() * (27 - 1 + 1) + 1);
      }
    }
    m = m < 10 ? '0' + m : m;
    d = d < 10 ? '0' + d : d;
    var ymd = '' + y + m + d;
    return ymd;
  }

  var s = Math.floor(Math.random() * 10).toString() + Math.floor(Math.random() * 10).toString() + Math.floor(Math.random() * 10).toString();

  var array = (address + birthday + s).split("");
  var total = 0;
  for (var i = 0; i < array.length; i++) {
    total = total + parseInt(array[i]) * parseInt(coefficientArray[i]);
  }
  var lastNumber = lastNumberArray[parseInt(total % 11)];
  var id_no_String = address + birthday + s + lastNumber;
  return id_no_String;
}



