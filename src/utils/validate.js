export function isvalidUsername(str) {
  const valid_map = ['admin', 'editor']
  return valid_map.indexOf(str.trim()) >= 0
}

// 是否是浮点数
export function isvalidFloatNumber(str) {
  const reg = /^(-?\d+)(\.\d+)?$/
  return reg.test(str)
}
// 是小数判断是否是两个以下的小数
export function isDoubleFloatNumber(str) {
  const reg = new RegExp('^((([1-9][0-9]+)|([0-9]))(.[0-9]{1,2})?)$')
  return reg.test(str)
}
